// ═══════════════════════════════════════════════════════════════════════════
//  Blockly 12.x JSON Serialization Structs
// ═══════════════════════════════════════════════════════════════════════════

// BlocklyWorkspace is the top-level struct that maps to the root of logic_json
// stored in strategy_versions.logic_json (Database Schema §4).
// It is generated by Blockly 12.x's serialization.workspaces.save() on the
// frontend and stored verbatim as JSONB in PostgreSQL.
type BlocklyWorkspace struct {
	Blocks WorkspaceBlocks `json:"blocks"`
}

// WorkspaceBlocks wraps the array of top-level blocks serialized by Blockly.
type WorkspaceBlocks struct {
	LanguageVersion int     `json:"languageVersion"`
	Blocks          []Block `json:"blocks"`
}

// Block represents a single node in the Blockly AST.
// It models both statement blocks (previousStatement/nextStatement chains)
// and value blocks (those with an output that feed into input slots).
type Block struct {
	// Type is the block's registered identifier (e.g., "event_on_candle",
	// "controls_if", "trade_smart_order"). Must exist in blockRegistry.
	Type string `json:"type"`

	// ID is the unique client-generated identifier assigned by Blockly.
	// Used for debugging and log correlation.
	ID string `json:"id"`

	// Fields holds scalar parameters configured directly on the block:
	// dropdown selections (string) and numeric field values (float64).
	// Examples: {"TRIGGER": "ON_CANDLE_CLOSE"}, {"LEVERAGE": 10}.
	// JSON numbers decode to float64 when unmarshaled into interface{}.
	Fields map[string]interface{} `json:"fields"`

	// Inputs holds connected child blocks — both value inputs (providing
	// a computed result) and statement inputs (inner block chains).
	// Examples: {"CONDITION": {...}, "DO": {...}, "QUANTITY": {...}}.
	Inputs map[string]BlockInput `json:"inputs"`

	// Next links to the following block in a statement chain.
	// Nil when this block is the last in its chain.
	Next *NextBlock `json:"next"`
}

// BlockInput represents one named input slot on a parent block.
// A slot can hold a live Block (user-connected) or a Shadow block (the Blockly
// default placeholder). Execution always uses Block; Shadow is parsed for
// completeness and may be used as a fallback in future extensions.
type BlockInput struct {
	Block  *Block `json:"block"`
	Shadow *Block `json:"shadow"`
}

// NextBlock wraps the pointer to the next statement block in a chain.
// Wrapping avoids a direct pointer-in-struct JSON cycle between Block.Next
// and Block itself.
type NextBlock struct {
	Block *Block `json:"block"`
}

