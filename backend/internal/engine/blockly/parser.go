// ═══════════════════════════════════════════════════════════════════════════
//  Parse & Validate
// ═══════════════════════════════════════════════════════════════════════════

// ParseLogicJSON unmarshals raw JSONB bytes from strategy_versions.logic_json
// (stored as []byte in domain.StrategyVersion.LogicJSON) into a traversable
// Block AST.
//
// It returns the root *Block — always an event_on_candle block — which serves
// as the entry point for the execution engine (ExecuteChain / ExecuteBlock).
// Returns a descriptive error if the JSON is malformed or the root block is
// not a valid event_on_candle.
func ParseLogicJSON(raw []byte) (*Block, error) {
	var workspace BlocklyWorkspace
	if err := json.Unmarshal(raw, &workspace); err != nil {
		return nil, fmt.Errorf("blockly: failed to unmarshal logic_json: %w", err)
	}

	if len(workspace.Blocks.Blocks) == 0 {
		return nil, ErrEmptyWorkspace
	}

	// QuantFlow strategies must have exactly one top-level block: event_on_candle.
	// Multi-root workspaces are not supported in v1.0.
	root := &workspace.Blocks.Blocks[0]
	if err := ValidateRootBlock(root); err != nil {
		return nil, err
	}

	return root, nil
}

// ValidateRootBlock ensures the given block is an event_on_candle block.
// Every valid QuantFlow strategy must begin with an event trigger block
// (SRS FR-RUN-02, blockly.md §3.1 — "required start block").
func ValidateRootBlock(root *Block) error {
	if root == nil {
		return ErrEmptyWorkspace
	}
	if root.Type != "event_on_candle" {
		return fmt.Errorf("%w: got %q", ErrInvalidRootBlock, root.Type)
	}
	return nil
}

// GetBlockMeta returns the registry metadata for the given block type string.
// Returns ErrUnknownBlockType if the type is not one of the 26 registered types.
// This is the authoritative check used by ExecuteBlock before dispatch.
func GetBlockMeta(blockType string) (BlockMeta, error) {
	meta, ok := blockRegistry[blockType]
	if !ok {
		return BlockMeta{}, fmt.Errorf("%w: %q", ErrUnknownBlockType, blockType)
	}
	return meta, nil
}

