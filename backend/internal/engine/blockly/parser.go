//  Safe Field & Input Accessors
// ═══════════════════════════════════════════════════════════════════════════

// GetInputBlock safely retrieves the connected Block from a named input slot.
//
// Blockly 12 input slots can hold two kinds of child block:
//   - Block  — a user-connected block (explicitly dragged into the slot).
//   - Shadow — a default placeholder block (set in the toolbox config).
//
// When the user edits a shadow block's fields (e.g., typing a number into
// the default math_number placeholder), Blockly serializes it under the
// "shadow" key — NOT under "block". If the user drags a separate block
// into the slot, Blockly stores that under "block" (the shadow is hidden).
//
// Execution semantics: use Block if present (user explicitly connected
// something), otherwise fall back to Shadow (the default placeholder value).
// Returns nil only when the slot is truly empty (no block or shadow).
func GetInputBlock(b *Block, inputName string) *Block {
	if b == nil || b.Inputs == nil {
		return nil
	}
	slot, ok := b.Inputs[inputName]
	if !ok {
		return nil
	}
	// Prefer user-connected block; fall back to shadow (default placeholder).
	if slot.Block != nil {
		return slot.Block
	}
	return slot.Shadow
}

// GetBodyBlock resolves the strategy body (first child statement) from the
// root event_on_candle block.
//
// The event_on_candle block definition uses `nextStatement` (not
// `input_statement "DO"`), so the strategy body is serialized under
// root.Next.Block in Blockly 12's JSON format — NOT under root.Inputs["DO"].
//
// This helper checks both locations for forward compatibility: Inputs["DO"]
// first (the original design assumption), then Next.Block (the actual format).
func GetBodyBlock(root *Block) *Block {
	if root == nil {
		return nil
	}
	// Preferred: check Inputs["DO"] (input_statement "DO" wiring)
	if body := GetInputBlock(root, "DO"); body != nil {
		return body
	}
	// Fallback: check Next (nextStatement wiring — actual Blockly 12 format)
	if root.Next != nil {
		return root.Next.Block
	}
	return nil
}

// GetFieldString safely retrieves a block field value as a string.
// Used for dropdown fields (e.g., TRIGGER, TIMEFRAME, OP, SIDE, ORDER_TYPE,
// PRICE_TYPE, FIELD, MARGIN_TYPE) which Blockly serializes as JSON strings.
// Returns an empty string if the field is absent or not a string type.
func GetFieldString(b *Block, fieldName string) string {
	if b == nil || b.Fields == nil {
		return ""
	}
	v, ok := b.Fields[fieldName]
	if !ok {
		return ""
	}
	s, _ := v.(string)
	return s
}

// GetFieldFloat safely retrieves a block field value as float64.
// Used for numeric fields (e.g., NUM, LEVERAGE, DECIMALS, value) which Blockly
// serializes as JSON numbers. encoding/json decodes JSON numbers into interface{}
// as float64 by default, so this type assertion is safe for standard JSON input.
// Returns 0 if the field is absent or cannot be asserted to float64.
func GetFieldFloat(b *Block, fieldName string) float64 {
	if b == nil || b.Fields == nil {
		return 0
	}
	v, ok := b.Fields[fieldName]
	if !ok {
		return 0
	}
	f, _ := v.(float64)
	return f
}
